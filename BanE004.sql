Operadores para producir valor booleano
Sea s un valor  onua tupla y R una relacion (Tabla)

1.- s IN R
2.- s > ANY R (s > SOME R) --> Operadores <,>,<=,>=,<>
3.- s > ALL R --> Operadores <,>,<=,>=,<>
4.- EXIST R

Estos operadores se pueden utilizar en su version negada

1.- s NOT IN R
2.- NOT s > ANY R (s > SOME R) --> Operadores <,>,<=,>=,<>
3.- NOT s > ALL R --> Operadores <,>,<=,>=,<>
4.- NOT EXIST R









